# Faça um programa que peça 2 numeros inteiros e mostre o maior deles.
# 
#
valor1=float(input("Insira um primeiro valor: "))
valor2=float(input("Insira um segundo valor: "))

print(f"Os valores inseridos foram: {valor1} e {valor2}.")

if valor1>valor2:

    print(f"O maior número é: {valor1}")

else:
    
    print(f"O maior número é: {valor2}")
