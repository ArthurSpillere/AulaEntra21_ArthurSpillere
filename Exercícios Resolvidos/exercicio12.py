# Crie um program que peça ao usuário que digite 2 números reias(float) e mostre:
# 
# A soma dos dois
# A multiplicação dos dois
# A divisão do primeiro com o segundo
# A subtração dos dois

num1=float(input('Insira um valor: '))
num2=float(input('Insira outro valor: '))

print(f"""
A soma é: {num1+num2}
A multiplicação é: {num1*num2}
A divisão do primeiro com o segundo é: {num1/num2}
A subtração é: {num1-num2}""")

