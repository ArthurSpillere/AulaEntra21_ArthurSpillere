# Escreva um programa que peça o peso (float) de 3 pessoas e motre na tela a soma de todos os pesos. 

peso1=float(input('Insira o peso da primeira pessoa:'))
peso2=float(input('Insira o peso da segunda pessoa:'))
peso3=float(input('Insira o peso da terceira pessoa:'))

somapeso=(peso1+peso2+peso3)

print('A soma dos pesos é de: {}'.format(somapeso))


